_REGISTRY = {
    "extraction":                 (_EXTRACTION_SYSTEM,                  _EXTRACTION_USER),
    "decomposition":              (_DECOMPOSITION_SYSTEM,               _DECOMPOSITION_USER),
    "query_generation":           (_QUERY_GENERATION_SYSTEM,            _QUERY_GENERATION_USER),
    "evidence":                   (_EVIDENCE_SYSTEM,                    _EVIDENCE_USER),
    "application":                (_APPLICATION_SYSTEM,                 _APPLICATION_USER),
    "counterargument":            (_COUNTERARGUMENT_SYSTEM,             _COUNTERARGUMENT_USER),
    "validation_consistency":     (_VALIDATION_CONSISTENCY_SYSTEM,      _VALIDATION_CONSISTENCY_USER),
    "aggregation_dependency":     (_AGGREGATION_DEPENDENCY_SYSTEM,      _AGGREGATION_DEPENDENCY_USER),
    "consistency_conflict":       (_CONSISTENCY_CONFLICT_SYSTEM,        _CONSISTENCY_CONFLICT_USER),
    "consistency_reconciliation": (_CONSISTENCY_RECONCILIATION_SYSTEM,  _CONSISTENCY_RECONCILIATION_USER),
    "confidence_justification":   (_CONFIDENCE_JUSTIFICATION_SYSTEM,    _CONFIDENCE_JUSTIFICATION_USER),
    "report":                     (_REPORT_SYSTEM,                      _REPORT_USER),
}


def get_prompt(name: str) -> tuple[str, str]:
    """Return (system_prompt, user_template) for the given prompt name.

    Raises KeyError if name is not registered.
    Available names:
        extraction, decomposition, evidence, application, counterargument,
        validation_consistency, aggregation_dependency, consistency_conflict,
        consistency_reconciliation, confidence_justification, report
    """
    if name not in _REGISTRY:
        raise KeyError(
            f"Unknown prompt '{name}'. Available: {sorted(_REGISTRY.keys())}"
        )
    return _REGISTRY[name]
